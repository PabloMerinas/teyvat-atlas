export const NODE_MAP = Object.fromEntries(NODES.map((n) => [n.id, n]));

export function nodeColor(node) {
  if (node.region && REGIONS[node.region]) return REGIONS[node.region].color;
  return ERAS[node.era].color;
}

export function spoilerIndex(key) {
  const i = SPOILER_ORDER.indexOf(key);
  return i === -1 ? 0 : i;
}

export function connectionsOf(nodeId) {
  return EDGES.filter((e) => e.source === nodeId || e.target === nodeId).map((e) => ({
    edge: e,
    otherId: e.source === nodeId ? e.target : e.source,
    direction: e.source === nodeId ? 'out' : 'in',
  }));
}
